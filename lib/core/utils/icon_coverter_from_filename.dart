import 'package:easy_download_manager/core/constant/app_icon.dart';
import 'package:flutter/material.dart';

IconData IconConverterFromFileName({required String filename}) {
  final extenstion = filename.split('.').last;

  bool isImage = _imageExtensions.contains(extenstion);
  bool isDocument = _documentExtensions.contains(extenstion);
  bool isAudio = _audioExtensions.contains(extenstion);
  bool isVideo = _videoExtensions.contains(extenstion);

  if (isImage) return AppIcon.imageIcon;
  if (isDocument) return AppIcon.documentIcon;
  if (isVideo) return AppIcon.videoIcon;
  if (isAudio) return AppIcon.audioIcon;

  return AppIcon.downloadIcon;
}

